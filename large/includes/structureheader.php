			<h5>Synergy of streptogramin antibiotics occurs independently of their effects on translation.</h5>
			<p><a href="#">Noeske, J.</a>, <a href="#">Huang, J..</a>, <a href="#">Olivier, N.B.</a>, <a href="#">Giacobbe, R.A.</a></p>
			<p><strong>Journal</strong>: (2014) Antimicrob.Agents Chemother. 58: 5269-5279 | <strong>PubMed</strong>: <a href="#">24957822</a></p>
			<p><strong>Abstract</strong>: Streptogramin antibiotics are divided into type A and B streptogramins, which in combination can act synergistically. We compared the molecular interactions of the streptogramin combinations Synercid (type A: dalfopristin, type B: quinupristin) and NXL 103 (type A: flopristin, type B <span class="collapse" id="collapseExample"> linopristin) with the Escherichia coli 70S ribosome by x-ray crystallography. We further analyzed the activity of the streptogramin components individually and in combination. Streptogramin A and B components in Synercid and NXL 103 exhibit synergistic antimicrobial activity against certain pathogenic bacteria. However, in transcription-coupled translation assays, only combinations that include dalfopristin, the streptogramin A component of Synercid, show synergy. Notably, the diethylaminoethylsulfonyl group in dalfopristin reduces its activity, but is the basis for synergy in transcription-coupled translation assays before its rapid hydrolysis from the depsipeptide core. Replacement of the diethylaminoethylsulfonyl group in dalfopristin by a non-hydrolyzable group may therefore be beneficial for synergy. The absence of general streptogramin synergy in transcription-coupled translation assays suggests that synergistic antimicrobial activity of streptogramins can occur independently of streptogramin effects on translation.</span><a data-toggle="collapse" href="#collapseExample" aria-expanded="true" aria-controls="collapseExample" class="small">[expand/collapse]</a></p> 
			
