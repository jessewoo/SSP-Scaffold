			<h3>Educational Curriculum</h3>
			<h5>Resources for teachers</h5>
			<p>The RCSB PDB Curricula were developed through collaborations and participation of scientists, curriculum design experts, educators, clinicians and local teachers to create authentic, hands-on teaching materials, individual and group activities and assessment suggestions. The curriculum also include relevant materials and activities related to the topic from existing public resources.</p>
		</div>	
		<div class="col-xs-12 col-sm-3 text-center center-block">
			<p><a href="#">2014/15 Curriculum: HIV/AIDS</a></p>
			<a href="http://education.rcsb.org/curriculum/">
				<img src="http://education.rcsb.org/curriculum/img/focus.png" class="img-responsive img-thumbnail">
			</a>
		</div>
		<div class="col-xs-12 col-sm-9">
			<p>The <a href="#">HIV/AIDS curriculum</a> is a classroom resource that introduces the structure and function of biological molecules that play key roles in immunity. In the context of these immune molecules, learn:</p>
			<ul>
			    <li>How and why is HIV infection so different from any other infection?</li>
			    <li>What are some of the current and upcoming strategies for treating HIV infection? Why is it important for the youth to understand the science of HIV/AIDS?</li>
			</ul>
			<p>Select teaching materials and activities from curricula at two different levels:</p>
			<ul>
			    <li>General Biology students (who have not yet received a detailed introduction to biological molecules); and</li>
			    <li>Advanced Placement Biology students ? who are learning biology at a 1st year college level</li>
			</ul>
