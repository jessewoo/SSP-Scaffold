		<h1>HIV: Human Immunodeficiency Virus</h1>
		<h4>The virus and how it infects cells</h4>
		<p>The AIDS epidemic began a few short decades ago and in that time, scientists have revealed the atomic structure of most of the molecules in the virus, and found effective ways to block them.</p>
		<p>HIV (human immunodeficiency virus) is composed of two strands of RNA, 15 types of viral proteins, and a few
proteins from the last host cell it infected, all surrounded by a lipid bilayer membrane. Together, these molecules allow the virus to infect cells of the immune system and force them to build new copies of the virus. Each molecule in the virus plays a role in this process, from the first steps of viral attachment to the final process of budding.</p>
		<p>25 years of research on the structural biology of HIV have revealed the atomic details of these proteins. These structures are all publicly available in the Protein Data Bank (PDB) archive. Using these data, researchers have designed new treatments for HIV infection, including effective drug regimens that halt the growth of the virus. The structures also provide new hope for development of a vaccine.</p>
		<p>To learn more about the molecules of HIV, explore PDB-101 and high quality external resources listed below:</p>
