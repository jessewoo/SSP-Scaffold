		<h3>Insulin Binding</h3>
		<p>When insulin binds to the receptor, it is thought to cause a change in shape that is propagated inside the cell, activating the tyrosine kinases. The details are still a mystery and an area of active research. A recent structure of insulin bound to a portion of the receptor (insulin shown here in red from PDB entry <a href="#">3w14</a>) places another piece in the puzzle. Surprisingly, insulin binds to the outer edge of the receptor, and typically only binds to one side of the symmetrical receptor.</p>
