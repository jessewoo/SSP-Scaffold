		<h3>Exploring the Structure (in 3D)</h3>
		<p>The tyrosine kinase portion of the receptor is itself a dynamic protein with many moving parts. The active site binds to ATP and uses it to phosphorylate its targets. In the inactive state (shown on the left, PDB entry <a href="#">1irk</a>), a mobile loop (in bright turquoise) binds in the active site, blocking its action. When the receptor is activated, several tyrosines (green) on this loop are phosphorylated, causing it to swing out of the active site, allowing ATP (magenta) to enter (shown on the right, PDB entry <a href="#">1ir3</a>). Other signaling proteins (a small peptide from one is shown in pink) then bind and are phosphorylated on their tyrosine amino acids. </p>
		<h4>Insulin Receptor Tyrosine Kinase (PDB entries 1irk and 1ir3)</h4>
		<p>These two structures show the tyrosine kinase domain of insulin receptor in the inactive and active states. A flexible loop is shown in bright turquoise which includes three tyrosines, shown in green, that are phosphorylated when the enzyme is activated. A short peptide, shown is pink, is bound in the active site of the active form, with a tyrosine positioned to be phosphorylated by ATP (magenta). </p>
