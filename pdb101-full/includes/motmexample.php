		<h1>Insulin Receptor</h1>
		<em>The cellular receptor for insulin helps control the utilization of glucose by cells.</em>
		<h5>February 2015 Molecule of the Month by <a href="#">David Goodsell</a></h5>
		<p><strong>DOI</strong>: <a href="#">10.2210/rcsb_pdb/mom_2015_2</a></p>
		<span class="label label-info">hormone action</span>
		<span class="label label-info">signal transduction</span>
		<span class="label label-info">blood glucose level</span>
		<span class="label label-info">diabetes mellitis</span>
		<span class="label label-info">receptor tyrosine kinase</span>
		<h3>Introduction</h3>
		<p>Cells throughout the body are fueled largely by glucose that is delivered through the bloodstream. A complex signaling system is used to control the process, ensuring that glucose is delivered when needed and stored when there is a surplus. Two hormones, insulin and glucagon, are at the center of this signaling system. When blood glucose levels drop, alpha cells in the pancreas release glucagon, which then stimulates liver cells to release glucose into the circulation. When blood glucose levels rise, on the other hand, beta cells in the pancreas release insulin, which promotes uptake of glucose for metabolism and storage. Both hormones are small proteins that are recognized by receptors on the surface of cells.</p>
		<h3>Signal Transduction</h3>
		<p>The receptor for insulin is a large protein that binds to insulin and passes its message into the cell. It has several functional parts. Two copies of the protein chains come together on the outside of the cell to form the receptor site that binds to insulin. This is connected through the membrane to two tyrosine kinases, shown here at the bottom. When insulin is not present, they are held in a constrained position, but when insulin binds, these constraints are released. They first phosphorylate and activate each other, and then phosphorylate other proteins in the signaling network inside the cell. Since the whole receptor is so flexible, researchers have determined its structure in several pieces: the insulin-binding portion is shown here from PDB entry <a href="#">3loh</a>, the transmembrane segment from <a href="#">2mfr</a>, and the tyrosine kinase from <a href="#">1irk</a>.</p>
		<h3>When Things Go Wrong</h3>
		<p>Problems with insulin signaling can impair the proper management of glucose levels in the blood, leading to the widespread disease diabetes mellitis. There are two common ways that this happens. Type I diabetes is caused by problems with insulin: in some cases, the pancreatic cells that produce insulin are destroyed by autoimmunity, and in other cases insulin is mutated and inactive. This often occurs early in life, and requires treatment with insulin to replace the missing insulin. Type II diabetes, on the other hand, most often occurs later in life, and is caused by an acquired resistance to the action of insulin on its receptor. The details are complex and involve phosphorylation of the receptor and its substrates, modifying their action in insulin signaling. The condition is treated with careful attention to diet, lifestyle and medication.</p>
