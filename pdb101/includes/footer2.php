				<p><strong>About PDB-101</strong></p>
				<p>Life is three-dimensional. This extends to life's molecular building blocks--proteins, DNA, and RNA. PDB-101 offers tools to explore the molecules of biological processes that define life.</p>
				<p>The Structural View of Biology interface starts with key topic categories and subcategories that drill down to individual molecules. It is built around the Molecule of the Month series, which regularly describes the structure and function of a molecule. Educational Resources provides activities and materials for learning, and Understanding PDB Data helps interpret the data archived in the PDB.</p>
				<p>The RCSB PDB develops these resources to support exploration of the structures found in the Protein Data Bank archive of experimentally-determined structures of proteins, nucleic acids, and complex assemblies.</p>
