	<h5>The Crystal Structure of Human Deoxyhaemoglobin at 1.74 &Aring; Resolution</h5>
	<p><a href="#">Fermi, G.</a>, <a href="#">Perutz, M.F.</a>, <a href="#">Shaanan, B.</a>, <a href="#">Fourme, R.</a></p>
	<p><strong>Journal</strong>: (1984) J.Mol.Biol. 175: 159-174 | <strong>PubMed</strong>: <a href="#">6726807</a></p>
	<p><strong>Abstract</strong>: The structure of human deoxyhaemoglobin was refined at 1.74 A resolution using data collected on film at room temperature from a synchrotron X-ray source. The crystallographic R-factor is 16.0%. The estimated error in atomic positions is 0.1 A overall, 0.14 for main-chain atoms of internal segments, and 0.05 A for the iron atoms. <a data-toggle="collapse" href="#collapseExample" aria-expanded="false" aria-controls="collapseExample" id="abstract-toggle"><span class="glyphicon glyphicon-plus" aria-hidden="true"></span></a> </p> 
	<p class="collapse" id="collapseExample">&Aring; The effects of intermolecular contacts on the structure were investigated; such contacts cause only highly localized distortions, as judged from the degree of molecular asymmetry that they induce. The geometry of the iron-nitrogen complex closely resembles that of the deoxymyoglobin structure of Takano (1977) and of the 5-co-ordinated model compounds of Hoard (1975) and Jameson et al. (1980). The distance of the iron from the mean plane of N(porphyrin) is 0.40(5) A and 0.36(5) A, respectively, at the alpha and beta haems, in contrast to the corresponding distance of +0.12(8) A and -0.11(8) A in oxyhaemoglobin (Shaanan, 1983); the Fe-N epsilon (F8) bond length is 2.12(4) A and the Fe-N(porphyrin) bond length is 2.06(2) A; the last is also in good agreement with extended X-ray fluorescence spectroscopy measurements on deoxyhaemoglobin ( Eisenberger et al., 1978; Perutz et al., 1982). The haems are domed toward the proximal side; the separation between the mean planes of N(porphyrin) and C(porphyrin) being 0.16(6) A and 0.10(6) A, respectively at the alpha and beta haems. At the alpha haems, the normals to the mean pyrrole planes are tilted uniformly toward the haem centre, by about three degrees relative to the haem normal, and there is a folding of about four degrees of the haem about an axis running between the methene carbons that are between the pyrrole rings bearing like-type side-chains. At the beta haems, there is no such folding, and only pyrroles II and IV (those eclipsed by His F8) are appreciably tilted, by about eight degrees. The independence of these parameters from restraints imposed on the model was verified by unrestrained refinement of the entire molecule starting from a structure with modified haem geometry. <a data-toggle="collapse" href="#collapseExample" aria-expanded="true" aria-controls="collapseExample"><span class="glyphicon glyphicon-minus" aria-hidden="true"></span></a></p>
	
	
